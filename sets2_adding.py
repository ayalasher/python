#we are going to add some Items to a set

fruits = {"apple","mango","banana","tangerine"}
cars = {"volvo","cayenne","fortuner","proton"}

fruits.add("orange")

print(fruits)#note that the order of the elements during printout will not be regular

fruits.update(cars)

print(fruits)

